package Pojo;

public class Evento {
	
	private int id;
	private String lugar;
	private String nomevento;
	private String tipoevento;
	private Fecha fecha;
	
	public int getId() {
		return id;
	}
	public String getLugar() {
		return lugar;
	}
	public String getNomevento() {
		return nomevento;
	}
	public String getTipoevento() {
		return tipoevento;
	}
	public Fecha getFecha() {
		return fecha;
	}
	
}
